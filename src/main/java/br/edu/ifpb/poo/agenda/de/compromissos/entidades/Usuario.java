package br.edu.ifpb.poo.agenda.de.compromissos.entidades;

import java.time.LocalDate;
import java.util.Objects;

/**
 *
 * @author Neg√£o
 */
public class Usuario extends Pessoa{
       private String email;
       private String senha;

    public Usuario(String email, String senha, String nome, LocalDate nascimento, String sexo) {
        super(nome, nascimento, sexo);
        this.email = email;
        this.senha = senha;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 67 * hash + Objects.hashCode(this.email);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Usuario other = (Usuario) obj;
        if (!Objects.equals(this.email, other.email)) {
            return false;
        }
        return true;
    } 

    public String toString(){
        return super.toString()+ "email:"+email+"senha:"+senha;
    }
}
